package com.Techzenure.Day_2;

public class AllDataTypes {

	public static void main(String[] args) {
		
		int num=10;   // Integer Data Type
		String name="Techzenure";  // String Data Type
		float num1=109.67f;   // Float Data Type
		
		System.out.println("Integer DataType : " +num);
		System.out.println("String DataType    : " +name);
		System.out.println("Float DataType      : " +num1);

	}

}
